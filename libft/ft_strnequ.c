#include "libft.h"

int	ft_strnequ(const char *s1, const char *s2, size_t n)
{
	if (!s1 || !s2)
		return (0);
	if (!n)
		return (1);
	while (*s1 && n)
	{
		if (*s1++ != *s2++)
			return (0);
		--n;
	}
	if (n && *s2)
		return (0);
	return (1);
}
